package com.evolving.apploader.android.sdk.datausage;

public class TData {
	private String sentBytes;
	private String recBytes;
	private long totalData;
	private String tcpBytesTX;
	private String udpBytesTX;
	private String tcpBytesRX;
	private String udpBytesRX;
	private String otherBytesTX;
	private String otherBytesRX;
	
	public String getSentBytes() {
		return sentBytes;
	}
	public void setSentBytes(String sentBytes) {
		this.sentBytes = sentBytes;
	}
	public String getRecBytes() {
		return recBytes;
	}
	public void setRecBytes(String recBytes) {
		this.recBytes = recBytes;
	}
	public String getTcpBytesTX() {
		return tcpBytesTX;
	}
	public void setTcpBytesTX(String tcpBytesTX) {
		this.tcpBytesTX = tcpBytesTX;
	}
	public String getUdpBytesTX() {
		return udpBytesTX;
	}
	public void setUdpBytesTX(String udpBytesTX) {
		this.udpBytesTX = udpBytesTX;
	}
	public String getTcpBytesRX() {
		return tcpBytesRX;
	}
	public void setTcpBytesRX(String tcpBytesRX) {
		this.tcpBytesRX = tcpBytesRX;
	}
	public String getUdpBytesRX() {
		return udpBytesRX;
	}
	public void setUdpBytesRX(String udpBytesRX) {
		this.udpBytesRX = udpBytesRX;
	}
	public String getOtherBytesTX() {
		return otherBytesTX;
	}
	public void setOtherBytesTX(String otherBytesTX) {
		this.otherBytesTX = otherBytesTX;
	}
	public String getOtherBytesRX() {
		return otherBytesRX;
	}
	public void setOtherBytesRX(String otherBytesRX) {
		this.otherBytesRX = otherBytesRX;
	}
	public long getTotalData() {
		return totalData;
	}
	public void setTotalData(long l) {
		this.totalData = l;
	}
	

}
